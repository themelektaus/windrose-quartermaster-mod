// Quartermaster minimal UE5.6 runtime helpers
// -------------------------------------------
// Hand-rolled UObject/UClass/UFunction layouts based on Dumper-7 output for
// Windrose-Win64-Shipping.exe (5.6.1-0+UE5-R5). We deliberately do NOT link the
// full 81MB Dumper-7 SDK - we only need the layouts + a few helpers to:
//
//   - locate GObjects in the running process
//   - iterate UObjects, resolve their FNames
//   - find a UClass by name (e.g. "R5HFSM_BuildingPanel")
//   - find a UFunction on that class by name (e.g. "GetBuildingGroupsByCategoryTag")
//
// All offsets in here are pulled from the Dumper-7 SDK headers, which were
// generated against the same exe we ship into - they should be exact for the
// current build of Windrose. If the game gets a binary update we'll need to
// re-dump and refresh the OFFSET_* constants below.

#pragma once

#include <stdint.h>

namespace QmUE
{
    using uint8  = uint8_t;
    using uint16 = uint16_t;
    using uint32 = uint32_t;
    using uint64 = uint64_t;
    using int8   = int8_t;
    using int16  = int16_t;
    using int32  = int32_t;
    using int64  = int64_t;

    // ---- Static offsets relative to Windrose-Win64-Shipping.exe image base ----
    // Generated by Dumper-7, see Tools/Dumper7/output/.../CppSDK/SDK/Basic.hpp
    // Dumper-7 build 5.6.1-0+UE5-R5, post-Steam-update 2026-05-18 09:52
    constexpr uintptr_t OFFSET_GObjects        = 0x10A570D0;
    constexpr uintptr_t OFFSET_AppendString    = 0x014E53F0;
    constexpr uintptr_t OFFSET_GWorld          = 0x10542CB0;
    constexpr uintptr_t OFFSET_ProcessEvent    = 0x017235E0;
    constexpr int32     PROCESS_EVENT_VTBL_IDX = 0x4C;

    // ---- FName layout (8 bytes) ----
    struct FName
    {
        int32 ComparisonIndex;
        uint32 Number;

        bool IsNone() const { return ComparisonIndex == 0 && Number == 0; }
        bool operator==(const FName& o) const { return ComparisonIndex == o.ComparisonIndex && Number == o.Number; }
        bool operator!=(const FName& o) const { return !(*this == o); }
    };
    static_assert(sizeof(FName) == 0x08, "FName must be 8 bytes");

    // ---- FString layout (16 bytes) ----
    // We use it only as an output parameter for AppendString. The game allocates,
    // we own a stack buffer that we pre-populate so the game writes into it.
    struct FString
    {
        wchar_t* Data;
        int32 Num;
        int32 Max;
    };
    static_assert(sizeof(FString) == 0x10, "FString must be 16 bytes");

    // ---- TArray<T> generic layout (16 bytes for any T*) ----
    // We only ever use the inline header (Data ptr + counters). T-specific
    // access happens via reinterpret_cast<T**>(Data).
    struct FTArrayHeader
    {
        void* Data;
        int32 Num;
        int32 Max;
    };
    static_assert(sizeof(FTArrayHeader) == 0x10, "FTArrayHeader must be 16 bytes");

    // ---- FGameplayTag is a single FName under the hood (8 bytes) ----
    // The native struct has a {FName TagName} member and is layout-identical to FName.
    using FGameplayTag = FName;

    // ---- UObject base layout (0x28 bytes) ----
    // UE5.6: vtable, flags, index, class, name, outer
    struct UObject
    {
        void*    VTable;        // 0x00
        uint32   Flags;         // 0x08 (EObjectFlags)
        int32    Index;         // 0x0C
        struct UClass* Class;   // 0x10
        FName    Name;          // 0x18
        UObject* Outer;         // 0x20
    };
    static_assert(sizeof(UObject) == 0x28, "UObject must be 0x28");

    // ---- UField extends UObject (+ 0x08 = 0x30) ----
    struct UField : public UObject
    {
        UField* Next;           // 0x28
    };
    static_assert(sizeof(UField) == 0x30, "UField must be 0x30");

    // ---- UStruct extends UField (size 0xB0) ----
    // 0x30 BaseChain (16B, 2 ptrs), 0x40 SuperStruct, 0x48 Children, ...
    struct UStruct : public UField
    {
        uint8 _pad_BaseChain[0x10];   // 0x30 FStructBaseChain (NumStructBasesInChainMinusOne + StructBaseChainArray)
        UStruct* SuperStruct;         // 0x40
        UField*  Children;            // 0x48
        void*    ChildProperties;     // 0x50
        int32    StructSize;          // 0x58
        int16    MinAlignment;        // 0x5C
        uint8    _pad_5E[0x52];       // 0x5E pad to 0xB0
    };
    static_assert(sizeof(UStruct) == 0xB0, "UStruct must be 0xB0");

    // ---- UClass extends UStruct (size 0x200) ----
    struct UClass : public UStruct
    {
        uint8  _pad_B0[0x28];           // 0xB0
        uint64 CastFlags;               // 0xD8 (EClassCastFlags)
        uint8  _pad_E0[0x30];           // 0xE0
        UObject* ClassDefaultObject;    // 0x110
        uint8  _pad_118[0xE8];          // 0x118 -> 0x200
    };
    static_assert(sizeof(UClass) == 0x200, "UClass must be 0x200");

    // ---- UFunction extends UStruct (size 0xE0) ----
    using FNativeFuncPtr = void(*)(void* Context, void* Stack, void* Result);
    struct UFunction : public UStruct
    {
        uint32 FunctionFlags;           // 0xB0
        uint8  _pad_B4[0x24];           // 0xB4
        FNativeFuncPtr ExecFunction;    // 0xD8
    };
    static_assert(sizeof(UFunction) == 0xE0, "UFunction must be 0xE0");

    // ---- EClassCastFlags values we use ----
    constexpr uint64 CASTFLAG_Class    = 0x00000020ULL;
    constexpr uint64 CASTFLAG_Function = 0x00080000ULL;

    // ---- ProcessEvent signature (UE5.6) ----
    // void UObject::ProcessEvent(UFunction* Function, void* Parms)
    // Method-style __thiscall on x64 maps to __fastcall(self, func, parms).
    using ProcessEventFn = void(__fastcall*)(UObject* self, UFunction* func, void* parms);

    // ---- GObjects layout ----
    struct FUObjectItem
    {
        UObject* Object;
        uint8 _pad[0x10];
    };
    static_assert(sizeof(FUObjectItem) == 0x18, "FUObjectItem must be 0x18");

    struct TUObjectArray
    {
        FUObjectItem** Objects;  // 0x00 chunked: Objects[chunk][indexInChunk]
        uint8 _pad_8[0x8];       // 0x08
        int32 MaxElements;       // 0x10
        int32 NumElements;       // 0x14
        int32 MaxChunks;         // 0x18
        int32 NumChunks;         // 0x1C

        static constexpr int32 ElementsPerChunk = 0x10000;

        UObject* GetByIndex(int32 idx) const
        {
            if (idx < 0 || idx >= NumElements) return nullptr;
            int32 chunk = idx / ElementsPerChunk;
            int32 inChunk = idx % ElementsPerChunk;
            if (chunk >= NumChunks) return nullptr;
            FUObjectItem* chunkPtr = Objects[chunk];
            if (!chunkPtr) return nullptr;
            return chunkPtr[inChunk].Object;
        }

        int32 Num() const { return NumElements; }
    };
    static_assert(sizeof(TUObjectArray) == 0x20, "TUObjectArray must be 0x20");

    // ---- Init / state ----
    // Call once from the worker thread. Returns false if any pointer fails to
    // resolve (no module-base, GObjects not yet allocated, etc).
    bool Init();

    // True after Init() returned true and GObjects has at least 1 entry.
    bool IsReady();

    // The TUObjectArray pointer (valid only after IsReady()).
    TUObjectArray* GetGObjects();

    // Resolve an FName to a wide-string. Returns false if AppendString isn't set
    // or the FName is None. out is a caller-provided buffer; outNum receives the
    // length excluding terminator.
    bool ResolveFName(const FName& name, wchar_t* outBuf, int32 outCap, int32& outNum);

    // Convenience: ResolveFName + narrow conversion to char[]. Returns true if
    // the name resolved and fit (incl. null terminator). On failure outBuf gets
    // an empty string.
    bool ResolveFNameNarrow(const FName& name, char* outBuf, int32 outCap);

    // Iterate GObjects, return the first UObject of class UClass whose own name
    // matches the given short name (e.g. "R5HFSM_BuildingPanel"). Returns
    // nullptr if not found.
    UClass* FindClassByName(const char* shortName);

    // Walk a UStruct's Children linked list, return the first UFunction whose
    // name matches funcName (e.g. "GetBuildingGroupsByCategoryTag"). Walks
    // SuperStruct chain too in case the function is inherited.
    UFunction* FindFunctionOnClass(UStruct* cls, const char* funcName);

    // Walk GObjects, return the first UObject whose class' short name equals
    // className and whose own short name equals objName (case-sensitive,
    // exact match). Returns nullptr if not found. Used to look up data assets
    // like R5BuildingItem 'DA_BI_QmBedrl_01' so we can read their package
    // and asset FNames for SoftPath redirect.
    UObject* FindObjectByClassAndName(const char* className, const char* objName);

    // Image base of Windrose-Win64-Shipping.exe (the main module). Cached
    // after Init().
    uintptr_t GetImageBase();

    // Resolved AppendString function pointer (signature is void(*)(const FName*, FString&)).
    // Returns nullptr if Init() not run.
    using AppendStringFn = void(*)(const FName*, FString*);
    AppendStringFn GetAppendStringFn();

    // Resolved ProcessEvent function pointer (UObject::ProcessEvent).
    // Returns nullptr if Init() not run. Used to call BlueprintCallable
    // UFunctions on a target UObject (typically a CDO).
    ProcessEventFn GetProcessEventFn();

    // Call ProcessEvent(self, func, parms) safely. Returns false if the call
    // raised an exception or ProcessEvent wasn't resolved. ALL UFunction calls
    // from our DLL go through this so we have one SEH boundary.
    bool CallProcessEvent(UObject* self, UFunction* func, void* parms);

    // Read UClass::ClassDefaultObject @ +0x110. Returns nullptr if cls is null
    // or the CDO hasn't been created yet (very rare for shipped builds).
    UObject* GetClassDefaultObject(UClass* cls);

    // Spawn a UObject of the given class via UGameplayStatics::SpawnObject
    // UFunction. This is the BlueprintCallable wrapper around NewObject<> -
    // we don't need to find StaticConstructObject_Internal ourselves.
    //
    // The UFunction lookup is cached after the first call. If outer is null,
    // we use the GameplayStatics CDO as the outer (which lives in the
    // transient package and is GC-rooted).
    //
    // Returns nullptr if anything fails (UFunction not found, ProcessEvent
    // not resolved, native exception during call, etc).
    UObject* SpawnObjectViaUFunction(UClass* objectClass, UObject* outer);

    // Convert a wide string into an FName via KismetStringLibrary::Conv_StringToName.
    // Equivalent to writing `FName(TEXT("foo"))` in native UE code - the FName
    // pool gets the string interned if not already present, and the resulting
    // {ComparisonIndex, Number} pair is returned.
    //
    // Used for the SoftPath override path: we construct FNames for the
    // PackageName and AssetName at runtime, so the spawned widget can point at
    // an asset that is NOT yet loaded into GObjects (the IoStore SoftRef
    // resolver will hydrate it on demand when the widget renders).
    //
    // Returns true if the call succeeded AND the resulting FName is non-None;
    // false on any failure (UFunction missing, ProcessEvent fault, empty input).
    // The first successful call caches the UFunction lookup for the lifetime
    // of the process.
    bool FNameFromString(const wchar_t* str, FName* outName);

} // namespace QmUE
